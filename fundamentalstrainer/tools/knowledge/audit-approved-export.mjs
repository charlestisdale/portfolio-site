#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { parseImportArgs, toProjectPath } from "../ingestion/import-transcript.mjs";

const args = parseImportArgs();
const root = process.cwd();
const inputFile = args.file;

function fail(message) {
  console.error(message);
  process.exit(1);
}

function asArray(value) {
  return Array.isArray(value) ? value : value ? [value] : [];
}

function words(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim()
    .split(/\s+/)
    .filter(Boolean);
}

function hasRepeatedPhrase(value) {
  const tokens = words(value);
  if (tokens.length < 8) return false;

  for (let size = 2; size <= 5; size += 1) {
    for (let index = 0; index + size * 2 <= tokens.length; index += 1) {
      const first = tokens.slice(index, index + size).join(" ");
      const second = tokens.slice(index + size, index + size * 2).join(" ");
      if (first && first === second) return true;
    }
  }

  return false;
}

function normalizeText(value) {
  return String(value || "").trim().toLowerCase().replace(/\s+/g, " ");
}

function auditObject(object, seenIds) {
  const errors = [];
  const warnings = [];
  const id = object.id || "unknown-id";
  const summary = object.learning?.summary || "";
  const explanation = object.learning?.explanation || "";
  const facts = asArray(object.learning?.facts).map(fact => typeof fact === "string" ? { text: fact } : fact);
  const sources = asArray(object.sources?.transcripts);
  const seedCounts = [
    asArray(object.assessmentSeeds?.examTips).length,
    asArray(object.assessmentSeeds?.commonMistakes).length,
    asArray(object.assessmentSeeds?.scenarios).length,
    asArray(object.assessmentSeeds?.pbqIdeas).length
  ].reduce((sum, count) => sum + count, 0);
  const relationshipCounts = [
    asArray(object.relationships?.prerequisites).length,
    asArray(object.relationships?.parents).length,
    asArray(object.relationships?.children).length,
    asArray(object.relationships?.related).length,
    asArray(object.relationships?.contrastsWith).length
  ].reduce((sum, count) => sum + count, 0);

  if (seenIds.has(id)) errors.push("duplicate object id in export");
  seenIds.add(id);

  if (!facts.length) errors.push("missing facts");
  if (facts.length > 0 && facts.length < 4) warnings.push("thin fact set; expected at least 4 facts for rich imports");
  if (summary.length < 80) warnings.push("summary is too short for a rich import");
  if (explanation.length < 160) warnings.push("explanation is too short for a rich import");
  if (!seedCounts) warnings.push("missing assessment seed material: exam tips, mistakes, scenarios, or PBQ ideas");
  if (!relationshipCounts) warnings.push("missing relationships");

  const firstFact = facts[0]?.text || "";
  if (normalizeText(summary) && normalizeText(summary) === normalizeText(firstFact)) warnings.push("summary duplicates first fact");
  if (hasRepeatedPhrase(summary) || hasRepeatedPhrase(explanation) || facts.some(fact => hasRepeatedPhrase(fact.text))) {
    errors.push("contains repeated transcript/caption fragments");
  }

  if (sources.length > 4 && facts.length <= 2) warnings.push("many transcript quotes but very little synthesized learning content");

  const badEvidenceOnlyPhrases = [
    "another popular file system you might run into is ext4",
    "that link is the operating system",
    "application has to be written for the operating system",
    "chrome os was created by google",
    "store a file, remove a file"
  ];
  const combined = normalizeText([summary, explanation, ...facts.map(fact => fact.text)].join(" "));
  if (badEvidenceOnlyPhrases.some(phrase => combined.includes(phrase))) {
    errors.push("contains known evidence-only transcript wording promoted as knowledge");
  }

  return { id, title: object.title || id, errors, warnings };
}

if (!inputFile) fail("Usage: node tools/knowledge/audit-approved-export.mjs --file=approved-knowledge-objects.json");

const sourcePath = path.resolve(root, inputFile);
if (!fs.existsSync(sourcePath)) fail(`Approved export not found: ${inputFile}`);

const exportData = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
const objects = asArray(exportData.objects || exportData);
if (!objects.length) fail("No objects found in export.");

const exportErrors = [];
const exportWarnings = [];
if (exportData.generatedBy === "browser-import-review-ui") {
  exportErrors.push("Export was generated by legacy browser-import-review-ui. Use the current pending candidate review flow instead.");
}
if (exportData.backupOnly) {
  exportWarnings.push("Export is a backup snapshot. It is useful for review state but should not be promoted directly as canonical knowledge.");
}

const seenIds = new Set();
const objectAudits = objects.map(object => auditObject(object, seenIds));
const failedObjects = objectAudits.filter(item => item.errors.length);
const warnedObjects = objectAudits.filter(item => item.warnings.length);
const passed = !exportErrors.length && !failedObjects.length;

const report = {
  generatedBy: "approved-export-auditor",
  source: toProjectPath(sourcePath, root),
  passed,
  objectCount: objects.length,
  exportErrors,
  exportWarnings,
  failedObjectCount: failedObjects.length,
  warnedObjectCount: warnedObjects.length,
  failedObjects,
  warnedObjects
};

console.log(JSON.stringify(report, null, 2));
process.exit(passed ? 0 : 1);
